-- Get available breeds for a pet species ID
function PCLcore.GetSpeciesBreeds(speciesID)
    if not speciesID or not PCLcore.breedData.species[speciesID] then
        return nil
    end
    return PCLcore.breedData.species[speciesID].breeds
end

-- Get breed name by ID
function PCLcore.GetBreedName(breedID)
    return PCLcore.breedNames[breedID] or 'Unknown'
end

-- Get breed stat modifiers
function PCLcore.GetBreedModifiers(breedID)
    return PCLcore.breedModifiers[breedID]
end

